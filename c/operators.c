/* With double numbers x, y, z you can ask C to do things like: */
/* x = y + z ;                         */
/* x = y / z ; /∗ Unless z is zero !                          */
/* x = y ∗ z ;                         */
/* x = y − z ;                         */
/* x++; /∗ Add one to x ∗/                         */
/* x −= x ; /∗ Subtract x from itself , funny way to say x = 0.0 ; */
/* x += z ; /∗ Add z onto x .                          */

/* For ints a, b and c we have the same options:                          */
/* a = b + c ;                         */
/* a = b − c ;                         */
/* a = b ∗ c ;                         */
/* a = b / c ; /∗ Careful now !                          */

